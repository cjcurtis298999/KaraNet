export function getPubStyle(pub) {
  return PUB_COLORS[pub] || { bg: "rgba(139,145,168,0.08)", border: "rgba(139,145,168,0.2)", text: "#8b91a8", dot: "#8b91a8" }
}

export function getAvatarGradient(idx) {
  const c = AVATAR_COLORS[idx % AVATAR_COLORS.length]
  return `linear-gradient(135deg, ${c[0]}, ${c[1]})`
}

export function getInitials(contact) {
  if (!contact.first || contact.first === "—") return "?"
  return (contact.first[0] + (contact.last ? contact.last[0] : "")).toUpperCase()
}

export function getAngleTags(angle) {
  if (!angle || angle === "—") return []
  return angle.split(/[\/,]/).map(s => s.trim()).filter(Boolean)
}
